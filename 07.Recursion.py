def fib(x):
    if x == 1:                     # Условие выход из рекурсии.
        return 0
    if x == 2:                     # Условие выход из рекурсии.
        return 1
    return fib(x - 1) + fib(x - 2)          # Рекурентная функция.
# -F- -----------------------------------------------------
#  *  -----------------------------------------------------
