def palindrom(s):
    if len(s) <= 1:                     # Если длина < или = 1 символу.
        # return True                               # РАБОТАЕТ !!!
        # return 'Строка является палиндромом.'     # РАБОТАЕТ !!!
        string = 'Строка является палиндромом !!!'
        return string
        # print('Строка является палиндромом')      # НЕ РАБОТАЕТ !!!
    if s[0] != s[-1]:                   # Условие выход из рекурсии.
        print(s)
        # return False                              # РАБОТАЕТ !!!
        return 'НЕ Палиндром.'                      # РАБОТАЕТ !!!
    print(s)
    return palindrom(s[1:- 1])          # Рекурентная функция.
# -F- -----------------------------------------------------
